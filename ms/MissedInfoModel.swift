import Foundation


class MissedInfoModel{
    
    var userid = ""
    var name = ""
    var photo = ""
    var facebook = ""
    var instagram = ""
    var linkedin = ""
    var snapchat = ""
    
    init(userid: String, name: String, photo: String, facebook: String, instagram: String, linkedin: String, snapchat: String){
        
        self.userid = userid
        self.name = name
        self.photo = photo
        self.facebook = facebook
        self.instagram = instagram
        self.linkedin = linkedin
        self.snapchat = snapchat
        
        
    }
    
    
    
    
    
}