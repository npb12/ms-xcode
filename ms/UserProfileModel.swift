import Foundation


class UserProfileModel{
    
    var name = ""
    var photo = ""
    var facebook_info = ""
    var instagram_info = ""
    var linkedin_info = ""
    var snapchat_info = ""
    
    
    init(name:String, photo:String, facebook_info:String, instagram_info:String, linkedin_info:String, snapchat_info:String){
        self.name = name
        self.photo = photo
        self.facebook_info = facebook_info
        self.instagram_info = instagram_info
        self.linkedin_info = linkedin_info
        self.snapchat_info = snapchat_info
    }
    
    
    
    
    
    
    
}